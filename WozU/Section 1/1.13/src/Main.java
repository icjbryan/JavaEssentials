
public class Main {

	public static void main(String[] args) {
		//The minimum and maximum values that a double can hold are 
		//about 4.9x10-324 and 1.8x10308 respectively. These are very precise values.
		double pi =3.14;
		
		//The minimum and maximum values that a float can hold are about 1.4x10-45 and 3.4x1038 respectively.
		float fpi = 3.14f;
		
		System.out.println(pi);
		System.out.println(fpi);
	}

}
